  const q = query.toLowerCase();
  return mockMovies.filter(m =>
    m.title.toLowerCase().includes(q) ||
    (m.originalTitle?.toLowerCase() || '').includes(q) ||
    m.genres.some(g => g.toLowerCase().includes(q))
  );
